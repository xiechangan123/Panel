// baseVhost Apache 虚拟主机基础实现
type baseVhost struct {
	config    *Config
	vhost     *Block // 主 VirtualHost 块
	configDir string
	siteName  string
}

// newBaseVhost 创建基础虚拟主机实例
func newBaseVhost(configDir string) (*baseVhost, error) {
	if configDir == "" {
		return nil, fmt.Errorf("config directory is required")
	}

	v := &baseVhost{
		configDir: configDir,
		siteName:  filepath.Base(filepath.Dir(configDir)),
	}

	// 从配置目录加载主配置文件
	var config *Config
	configFile := filepath.Join(configDir, "apache.conf")
	if _, err := os.Stat(configFile); err == nil {
		config, err = ParseFile(configFile)
		if err != nil {
			return nil, fmt.Errorf("failed to parse apache config: %w", err)
		}
	}

	// 没有配置文件则使用默认配置
	if config == nil {
		var err error
		config, err = ParseString(v.defaultConf())
		if err != nil {
			return nil, fmt.Errorf("failed to parse default config: %w", err)
		}
	}

	v.config = config
	if vhosts := config.VirtualHosts(); len(vhosts) > 0 {
		v.vhost = vhosts[0]
	} else {
		v.vhost = config.AddVirtualHost("*:80")
	}

	return v, nil
}

// defaultConf 返回替换好站点名的默认配置模板
func (v *baseVhost) defaultConf() string {
	return strings.ReplaceAll(DefaultVhostConf, "/opt/ace/sites/default", fmt.Sprintf("/opt/ace/sites/%s", v.siteName))
}

// NewStaticVhost 创建纯静态虚拟主机实例
func NewStaticVhost(configDir string) (*StaticVhost, error) {
	base, err := newBaseVhost(configDir)
	if err != nil {
		return nil, err
	}
	return &StaticVhost{baseVhost: base}, nil
}

