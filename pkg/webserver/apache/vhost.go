// ========== PHPVhost ==========

func (v *PHPVhost) PHP() uint {
	cfg, err := ParseFragment(v.Config("010-php.conf", types.ScopeSite))
	if err != nil {
		return 0
	}
	d := cfg.FindOne("FilesMatch.SetHandler")
	if d == nil || len(d.Args) == 0 {
		return 0
	}
	return phpVersionFromHandler(d.Args[0].Value)
}

func (v *PHPVhost) SetPHP(version uint) error {
	if version == 0 {
		return v.RemoveConfig("010-php.conf", types.ScopeSite)
	}

	// sock 路径格式: proxy:unix:/tmp/php-cgi-84.sock|fcgi://localhost/
	handler := fmt.Sprintf("proxy:unix:/tmp/php-cgi-%d.sock|fcgi://localhost/", version)
	cfg := &Config{}
	cfg.Append(Blk("FilesMatch", `\.php$`).Append(
		&Directive{Name: "SetHandler", Args: []Argument{dquote(handler)}},
	))
	return v.SetConfig("010-php.conf", "site", cfg.Export()+"\n")
}

// phpVersionFromHandler 从 SetHandler 值提取 PHP 版本号
func phpVersionFromHandler(handler string) uint {
	idx := strings.Index(handler, "php-cgi-")
	if idx == -1 {
		return 0
	}
	var version uint
	if _, err := fmt.Sscanf(handler[idx:], "php-cgi-%d.sock", &version); err != nil {
		return 0
	}
	return version
}

