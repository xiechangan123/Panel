func (v *baseVhost) Save() error {
	configFile := filepath.Join(v.configDir, "apache.conf")
	if err := os.WriteFile(configFile, []byte(v.config.Render()+"\n"), 0600); err != nil {
		return fmt.Errorf("failed to save config file: %w", err)
	}
	return nil
}

func (v *baseVhost) Reset() error {
	config, err := ParseString(v.defaultConf())
	if err != nil {
		return fmt.Errorf("failed to reset config: %w", err)
	}
	v.config = config
	if vhosts := config.VirtualHosts(); len(vhosts) > 0 {
		v.vhost = vhosts[0]
	}
	return nil
}

func (v *baseVhost) Config(name string, scope types.ConfigScope) string {
	content, err := os.ReadFile(filepath.Join(v.configDir, string(scope), name))
	if err != nil {
		return ""
	}
	return strings.TrimSpace(string(content))
}

func (v *baseVhost) SetConfig(name string, scope types.ConfigScope, content string) error {
	return v.writeConfig(name, scope, "# Auto-generated by AcePanel. DO NOT EDIT MANUALLY!\n"+content)
}

func (v *baseVhost) SetRawConfig(name string, scope types.ConfigScope, content string) error {
	return v.writeConfig(name, scope, content)
}

func (v *baseVhost) writeConfig(name string, scope types.ConfigScope, content string) error {
	if err := os.WriteFile(filepath.Join(v.configDir, string(scope), name), []byte(content), 0600); err != nil {
		return fmt.Errorf("failed to write config file: %w", err)
	}
	return nil
}

func (v *baseVhost) RemoveConfig(name string, scope types.ConfigScope) error {
	if err := os.Remove(filepath.Join(v.configDir, string(scope), name)); err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("failed to remove config file: %w", err)
	}
	return nil
}

func (v *baseVhost) SSL() bool {
	return v.vhost.Has("SSLEngine") && strings.EqualFold(v.vhost.Value("SSLEngine"), "on")
}

func (v *baseVhost) SSLConfig() *types.SSLConfig {
	if !v.SSL() {
		return nil
	}

	config := &types.SSLConfig{
		Cert: v.vhost.Value("SSLCertificateFile"),
		Key:  v.vhost.Value("SSLCertificateKeyFile"),
	}
	if protocols := v.vhost.Values("SSLProtocol"); protocols != nil {
		config.Protocols = protocols
	}

	for _, h := range v.vhost.GetAll("Header") {
		if isHSTSHeader(h) {
			config.HSTS = true
			break
		}
	}
	config.OCSP = strings.EqualFold(v.vhost.Value("SSLUseStapling"), "on")
	for _, r := range v.vhost.GetAll("RewriteRule") {
		if argsContain(r.Args, "https://") {
			config.HTTPRedirect = true
			break
		}
	}

	return config
}

func (v *baseVhost) SetSSLConfig(cfg *types.SSLConfig) error {
	if cfg == nil {
		return fmt.Errorf("SSL config cannot be nil")
	}

	v.vhost.Set("SSLEngine", "on")
	if cfg.Cert != "" {
		v.vhost.Set("SSLCertificateFile", cfg.Cert)
	}
	if cfg.Key != "" {
		v.vhost.Set("SSLCertificateKeyFile", cfg.Key)
	}

	if len(cfg.Protocols) > 0 {
		v.vhost.Set("SSLProtocol", cfg.Protocols...)
	} else {
		v.vhost.Set("SSLProtocol", "all", "-SSLv2", "-SSLv3", "-TLSv1", "-TLSv1.1")
	}

	// HSTS：先移除旧的 HSTS Header 再添加，保留其他 Header
	if cfg.HSTS {
		v.vhost.RemoveFunc("Header", isHSTSHeader)
		v.vhost.Add("Header", "always", "set", "Strict-Transport-Security", HSTSValue)
	}

	if cfg.OCSP {
		v.vhost.Set("SSLUseStapling", "on")
	}

	// HTTP 强制跳转 HTTPS
	if cfg.HTTPRedirect {
		v.vhost.Set("RewriteEngine", "on")
		v.vhost.Add("RewriteCond", "%{HTTPS}", "off")
		v.vhost.Add("RewriteRule", "^(.*)$", "https://%{HTTP_HOST}%{REQUEST_URI}", "[R=301,L]")
	}

	// 确保监听 443 端口
	if !v.hasPort("443") {
		v.vhost.AppendArg("*:443")
	}

	return nil
}

func (v *baseVhost) ClearSSL() error {
	for _, name := range []string{"SSLEngine", "SSLCertificateFile", "SSLCertificateKeyFile", "SSLProtocol", "SSLCipherSuite", "SSLUseStapling"} {
		v.vhost.Remove(name)
	}
	v.vhost.RemoveFunc("Header", isHSTSHeader)
	v.vhost.Remove("RewriteEngine")
	v.vhost.RemoveAll("RewriteCond")
	v.vhost.RemoveAll("RewriteRule")

	// 移除 443 监听端口
	var newArgs []string
	for _, addr := range v.vhost.ArgValues() {
		if portOf(addr) != "443" {
			newArgs = append(newArgs, addr)
		}
	}
	if len(newArgs) == 0 {
		newArgs = []string{"*:80"}
	}
	v.vhost.SetArgs(newArgs...)
	return nil
}

// hasPort 判断 VirtualHost 是否监听指定端口
func (v *baseVhost) hasPort(port string) bool {
	return slices.ContainsFunc(v.vhost.ArgValues(), func(addr string) bool {
		return portOf(addr) == port
	})
}

func (v *baseVhost) RateLimit() *types.RateLimit {
	if v.vhost.Value("SetOutputFilter") != "RATE_LIMIT" {
		return nil
	}

	rateLimit := &types.RateLimit{}
	for _, d := range v.vhost.GetAll("SetEnv") {
		vals := argValues(d.Args)
		if len(vals) >= 2 && vals[0] == "rate-limit" {
			rateLimit.Rate, _ = strconv.Atoi(vals[1])
		}
	}
	return rateLimit
}

func (v *baseVhost) SetRateLimit(limit *types.RateLimit) error {
	// Apache mod_ratelimit 只支持流量限制，不支持并发连接限制
	if limit.Rate > 0 {
		v.vhost.Set("SetOutputFilter", "RATE_LIMIT")
		v.vhost.Set("SetEnv", "rate-limit", strconv.Itoa(limit.Rate))
	} else {
		v.vhost.Remove("SetOutputFilter")
		v.vhost.RemoveAll("SetEnv")
	}
	return nil
}

func (v *baseVhost) ClearRateLimit() error {
	v.vhost.Remove("SetOutputFilter")
	v.vhost.RemoveAll("SetEnv")
	return nil
}

func (v *baseVhost) BasicAuth() map[string]string {
	if !strings.EqualFold(v.vhost.Value("AuthType"), "Basic") {
		return nil
	}
	return map[string]string{
		"realm":     v.vhost.Value("AuthName"),
		"user_file": v.vhost.Value("AuthUserFile"),
	}
}

func (v *baseVhost) SetBasicAuth(auth map[string]string) error {
	realm := auth["realm"]
	if realm == "" {
		realm = "Restricted"
	}
	v.vhost.Set("AuthType", "Basic")
	v.vhost.Set("AuthName", realm)
	v.vhost.Set("AuthUserFile", auth["user_file"])
	v.vhost.Set("Require", "valid-user")
	return nil
}

func (v *baseVhost) ClearBasicAuth() error {
	for _, name := range []string{"AuthType", "AuthName", "AuthUserFile", "Require"} {
		v.vhost.Remove(name)
	}
	return nil
}

func (v *baseVhost) RealIP() *types.RealIP {
	// Apache 使用 mod_remoteip
	header := v.vhost.Value("RemoteIPHeader")
	if header == "" {
		return nil
	}

	var from []string
	for _, d := range v.vhost.GetAll("RemoteIPTrustedProxy") {
		if len(d.Args) > 0 {
			from = append(from, d.Args[0].Value)
		}
	}

	return &types.RealIP{From: from, Header: header}
}

func (v *baseVhost) SetRealIP(realIP *types.RealIP) error {
	v.vhost.Remove("RemoteIPHeader")
	v.vhost.RemoveAll("RemoteIPTrustedProxy")

	if realIP == nil || (len(realIP.From) == 0 && realIP.Header == "") {
		return nil
	}

	if realIP.Header != "" {
		v.vhost.Set("RemoteIPHeader", realIP.Header)
	}
	for _, ip := range realIP.From {
		if ip != "" {
			v.vhost.Add("RemoteIPTrustedProxy", ip)
		}
	}
	return nil
}

func (v *baseVhost) ClearRealIP() error {
	v.vhost.Remove("RemoteIPHeader")
	v.vhost.RemoveAll("RemoteIPTrustedProxy")
	return nil
}

